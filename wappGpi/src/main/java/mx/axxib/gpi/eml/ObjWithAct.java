package mx.axxib.gpi.eml;

public class ObjWithAct {

	private Integer idObjetivo;
	private Integer numObjetivos;
	private String objetivo;
	private Integer idAccion;
	private Integer numAcciones;
	private String accion;
	
	
	public Integer getNumObjetivos() {
		return numObjetivos;
	}
	public void setNumObjetivos(Integer numObjetivos) {
		this.numObjetivos = numObjetivos;
	}
	public Integer getNumAcciones() {
		return numAcciones;
	}
	public void setNumAcciones(Integer numAcciones) {
		this.numAcciones = numAcciones;
	}
	public Integer getIdObjetivo() {
		return idObjetivo;
	}
	public void setIdObjetivo(Integer idObjetivo) {
		this.idObjetivo = idObjetivo;
	}
	public String getObjetivo() {
		return objetivo;
	}
	public void setObjetivo(String objetivo) {
		this.objetivo = objetivo;
	}
	public Integer getIdAccion() {
		return idAccion;
	}
	public void setIdAccion(Integer idAccion) {
		this.idAccion = idAccion;
	}
	public String getAccion() {
		return accion;
	}
	public void setAccion(String accion) {
		this.accion = accion;
	}
	
	
}
